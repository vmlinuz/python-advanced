"""
Завдання:
    Обробити дуже великиий лог-файл і знайти топ користувачів за сумою value.

Пояснення:
    Файл має розмір десятки гігабайтів, тому його неможливо повністю завантажити в памʼять.
    Рішення повинно працювати у стрімінговому режимі та мінімізувати використання RAM.

    Основна складність - правильно обрати структуру даних для накопичення
    проміжної статистики та ефективного визначення top-N користувачів.

Вимоги:
    - Читання файлу повинно бути потоковим (англ. streaming);
    - Заборонено читати файл повністю в памʼять;
    - Потрібно коректно обробляти пошкоджені рядки;
    - Значення value можуть бути відʼємними;
    - Один user_id може повторюватися дуже багато разів;
    - Повернути top-N користувачів за сумарним value;
    - Рішення має бути максимально ефективним по памʼяті.

Що слід реалізувати:
    - streaming-читання файлу;
    - акумуляцію сум по user_id;
    - обробку невалідних рядків;
    - ефективний пошук top-N;
    - обґрунтований вибір структури даних.

Примітка:
    Просте рішення через read(), readlines(), pandas.read_csv() без чанків -
    вважається неефективним для цієї задачі.

Актуальність:
    Це типовий production-кейс у Big Data та backend-системах. Подібні задачі
    виникають під час обробки clickstream-логів, фінансових транзакцій,
    телеметрії, security-івентів та ETL-пайплайнів.

    У реальних системах правильний вибір структури (dict vs Counter vs heap
    vs external aggregation) і стрімінгова обробка можуть зменшити споживання
    памʼяті в десятки разів і зробити задачу взагалі виконуваною.
"""


def top_users(file_path: str, top_n: int = 5) -> list[tuple[str, float]]:
    import heapq

    # Словник для акумуляції сум по user_id
    totals: dict[str, float] = {}

    # Стрімінгове читання файлу рядок за рядком
    with open(file_path, 'r', encoding='utf-8') as f:
        for line in f:
            line = line.strip()
            if not line:
                continue

            fields = line.split(',')

            # Пропускаємо пошкоджені рядки (очікуємо рівно 4 поля)
            if len(fields) != 4:
                continue

            try:
                user_id = fields[1]
                value = float(fields[3])
            except (ValueError, IndexError):
                # Пропускаємо рядки з нечисловим value
                continue

            # Акумулюємо суму для кожного користувача
            totals[user_id] = totals.get(user_id, 0.0) + value

    # Ефективний вибір top-N через heapq (O(n log k) замість O(n log n))
    return heapq.nlargest(top_n, totals.items(), key=lambda x: x[1])
