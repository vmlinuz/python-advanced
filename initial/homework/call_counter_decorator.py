"""
Завдання: Декоратор підрахунку викликів функції

Мета:
    Освіжити знання про:
    - декоратори в Python;
    - замикання (англ. closures);
    - збереження стану між викликами.

Контекст:
    У багатьох реальних системах важливо знати:
    - скільки разів була викликана функція;
    - чи використовується вона взагалі;
    - як зберігати стан без глобальних змінних.

    Ця задача демонструє, як декоратор може зберігати стан між викликами функції.

Умова:
    Реалізуйте декоратор @call_counter, який:
    - підраховує кількість викликів функції;
    - додає до функції атрибут calls;
    - при кожному виклику збільшує цей лічильник;
    - повертає оригінальний результат функції.

    Декоратор не повинен нічого друкувати.

Вимоги:
    - використовуйте вкладену функцію (wrapper);
    - не використовуйте глобальні змінні;
    - лічильник має зберігатися в замиканні або атрибуті функції;
    - підтримуйте *args та **kwargs.

Приклад:
    @call_counter
        def add(a, b):
            return a + b

    >>> add(1, 2)
    3
    >>> add(3, 4)
    7
    >>> add.calls
    2
"""

from typing import Callable


def call_counter(func: Callable) -> Callable:
    calls = 0

    def wrapper(*args, **kwargs):
        nonlocal calls
        calls += 1
        wrapper.calls = calls
        return func(*args, **kwargs)

    wrapper.calls = calls
    return wrapper

