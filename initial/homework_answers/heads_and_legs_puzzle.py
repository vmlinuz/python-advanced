def solve_heads_and_legs(num_heads: int, num_legs: int) -> tuple[int, int]:
    """
    1. Часова складність: O(n)
    Алгоритм перебирає всі можливі значення кількості курей від 0 до num_heads включно. Для кожної ітерації виконується
    стала кількість арифметичних операцій і одне порівняння. У найгіршому випадку (коли розвʼязку не існує) цикл
    проходить усі num_heads + 1 ітерацій. Тому кількість виконаних операцій лінійно залежить від кількості голів, і
    часова складність алгоритму дорівнює O(n), де n = num_heads.

    2. Просторова складність: O(1)
    Функція не створює жодних додаткових структур даних, розмір яких залежить від вхідних параметрів. Вона використовує
    лише кілька локальних змінних (chickens, rabbits) і повертає кортеж фіксованого розміру. Отже, додаткова памʼять,
    необхідна для виконання алгоритму, є сталою - O(1).
    """
    for chickens in range(num_heads + 1):
        rabbits = num_heads - chickens
        if chickens * 2 + rabbits * 4 == num_legs:
            return chickens, rabbits
    return -1, -1
