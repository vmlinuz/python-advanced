def even_squares(numbers: list[int]) -> list[int]:
    """
    1. Часова складність: O(n)
    Функція послідовно обробляє всі елементи вхідного списку numbers. Операція filter перевіряє кожен елемент на
    парність рівно один раз, а map застосовує операцію піднесення до квадрату також один раз для кожного
    відфільтрованого елемента. Хоча filter і map є лінивими ітераторами, при приведенні результату до списку
    (list(squares)) відбувається повна ітерація по всій вхідній послідовності. Таким чином, кожен елемент обробляється
    сталою кількістю операцій, і загальна часова складність алгоритму лінійна - O(n).

    2. Просторова складність: O(n)
    Попри те, що filter і map самі по собі не створюють проміжних списків і працюють у лінивому режимі, фінальний
    виклик list(squares) матеріалізує всі результати в памʼяті. У найгіршому випадку (коли всі числа парні) розмір
    вихідного списку пропорційний розміру вхідного списку. Отже, додаткова памʼять, яку використовує функція, зростає
    лінійно від n, і просторова складність дорівнює O(n).
    """
    evens = filter(lambda x: x % 2 == 0, numbers)
    squares = map(lambda x: x**2, evens)
    return list(squares)
